, pixels AS (
select f.id, col, row, f."color_id"
from(
    select
     id.*,
     case when coalesce(a."attest_valid",false) then m."magic" else 4 END as "color_id"
     from all_ids id
     LEFT JOIN attested a
     ON a."id" = id."id" and a."attest_valid"
     LEFT JOIN magic m
     ON m."id" = id."id"
) f
)

, color_channels AS (
 select
 y."id" as "row",
 x."id" as "column",
 c."id" as "color",
 10*(y."id"::int) + c."id" as "chann_order",
 concat((y."id"),'_',col."hex") as "channel"
 from
 (select * from generate_series(0, 32) as id) y
 cross join
 (select * from generate_series(0, 32) as id) x
 cross join
 (select * from generate_series(0, 4) as id) c
 left join colors col
 ON c."id" = col."id"
 order by chann_order DESC
)

SELECT "channel", "column", "signal" FROM
(
    SELECT
    c."channel",
    c."column",
    c."chann_order",
    case when p."id" > 0 THEN 1 ELSE 0 END as signal
    FROM color_channels c
    LEFT JOIN pixels p
    ON c.row = p.row AND c."column" = p."col" AND p."color_id" = c."color"
) sig
WHERE "signal" = 1 OR "column" = 0
ORDER BY "chann_order" DESC
